package com.Tnsif.firstproject;

public class Typecoversion {
	public static void main(String args[]) {
		/*implicit conversion
		 * int to float value conversion
		 */
	int a=30;
	float b=a;
	System.out.println(b);
	/*
	 * explicit conversion
	 * float to int value conversion
	 * 
	 */
	double c=20.5;
	int d=(int)c;
	System.out.println(d);
	 
	}

}
